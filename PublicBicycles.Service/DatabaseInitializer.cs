        /// <summary>
        /// 生成测试数据
        /// </summary>
        /// <param name="context"></param>
        /// <returns></returns>
        public static void GenerateTestDatas(PublicBicyclesContext db,int days)
        {
            Debug.WriteLine("开始生成测试数据");
            db.Database.EnsureDeleted();
            db.Database.EnsureCreated();

            HireService.SaveChanges = false;


            Debug.WriteLine("开始添加用户");
            List<User> users = new List<User>();
            db.Add(new User()
            {
                Username = "admin",
                Password = UserService.CreateMD5("admin"),
                IsAdmin = true,
            });
            for (int i = 0; i < 1000; i++)
            {
                users.Add(new User()
                {
                    Username = "user" + (i + 1),
                    Password = UserService.CreateMD5("1234"),
                });
            }
            db.AddRange(users);


            Debug.WriteLine("开始添加租赁点与自行车");
            int bicycleID = 1;
            List<Station> stations = new List<Station>();
            List<Bicycle> bicycles = new List<Bicycle>();
            foreach (var line in stationTable.Split(new string[] { "\r\n" }, StringSplitOptions.RemoveEmptyEntries))
            {
                string[] items = line.Split(' ');
                var station = new Station()
                {
                    Name = items[1],
                    Count = int.Parse(items[2]),
                    Address = items[3],
                    Lat = double.Parse(items[6]),
                    Lng = double.Parse(items[7]),
                };
                stations.Add(station);
                for (int i = 0; i < station.Count / 2; i++)
                {
                    Bicycle bicycle = new Bicycle() { Station = station, BicycleID=(bicycleID++) };
                    bicycles.Add(bicycle);
                }
                station.BicycleCount = station.Count / 2;
            }
            db.AddRange(stations);
            db.AddRange(bicycles);
            db.SaveChanges();


            Debug.WriteLine("开始生成借车记");
            Random r = new Random();
            List<Hire> hirings = new List<Hire>();
            DateTime fromDate = DateTime.Today.AddDays(-days);
            DateTime toDate = DateTime.Today;
            for (DateTime date = fromDate; date < toDate; date = date.AddDays(1))
            {
                DateTime fromTime = date.AddHours(6);
                DateTime toTime = date.AddHours(22);
                for (DateTime time = fromTime; time < toTime; time = time.AddMinutes(1))
                {
                    HireService.Now = () => time;
                    bool hiredOrReturn = false;
                    if (time.Hour >= 7 && hirings.Count > 5)
                    {
                         hiredOrReturn = true;
                        Hire hire = hirings[r.Next(0, hirings.Count - 1)];
                        var p1 = new Point(hire.HireStation.Lng, hire.HireStation.Lat) { SRID = 4326 };
                        Station station = null;
                        double distance = 0;
                        do
                        {
                            station = stations[r.Next(0, stations.Count - 1)];
                            var p2 = new Point(station.Lng, station.Lat) { SRID = 4326 };
                            distance = FzLib.Geography.Analysis.Calculate.Distance(p1, p2);
                            //借车和还车的车站距离需要小于5000米
                        } while (distance > 5000);
                        var @return = HireService.ReturnAsync(db, hire.Hirer.ID, hire.Bicycle.ID, station.ID).Result;
                        if (@return.Type == ReturnResultType.Succeed)
                        {
                            hirings.Remove(hire);
                            Debug.WriteLine(time.ToString() + "   还车，当前正在借车数量：" + hirings.Count);
                        }
                    }

                    if (hirings.Count < 100 && time.Hour < 21)
                    {
                        hiredOrReturn = true;
                        Station station = stations[r.Next(0, stations.Count - 1)];
                        Bicycle bicycle = db.Bicycles.Where(p => p.Station == station && !p.Hiring).First();
                        var hire = HireService.HireAsync(db, users[r.Next(0, users.Count)].ID, bicycle.ID, station.ID).Result;
                        if (hire.Type == HireResultType.Succeed)
                        {
                            hirings.Add(hire.Hire);
                            Debug.WriteLine(time.ToString() + "   借车，当前正在借车数量：" + hirings.Count);
                        }
                    }
                    if(!hiredOrReturn )
                    {

                    }
                    db.SaveChanges();
                }
            }
            HireService.Now = () => DateTime.Now;
            HireService.SaveChanges = true;
            db.SaveChanges();
            Debug.WriteLine("完成");
        }

